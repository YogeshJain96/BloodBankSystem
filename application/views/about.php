  <p class="lead">Problem Statement</p>	
  <hr class="my-4">
  <p>Design a simple <b>‘Blood Bank System’</b> web application:<br>
<b>Requirements:</b><br>
1) Assume you are designing a real-life system, that will be used by real users.<br>
2) The application should contain 2 types of users: Hospitals and Receivers<br>
3) Pages to be developed-
‘Registration’ pages - Different registration pages for hospitals & receivers. Capture receiver’s blood group
during registration.
‘Login’ pages - Single/different login pages for hospitals & receivers.Hospital ‘Add blood info’ page - A hospital,
once logged in, should be able to add details of available blood samples (along with type) to their bank.
Access to this page should be restricted only to hospitals.
‘Available blood samples’ page - There should be a page that displays all the available blood samples along
with which hospital has them and a ‘Request Sample’ button. This page should be accessible to everyone,
irrespective of whether the user is logged in or not. Expected functionality on click of the 'Request Sample'
button-
Only receivers should be able to request for blood samples by clicking the ‘Request Sample’ button.
Make sure that only those receivers who are eligible for the blood sample are allowed to click the
button.<br>
If the user is not logged in, then he/she should be redirected to the login page.
If a user is logged in as a hospital, then the user should not be allowed to request for a blood sample.<br>
Hospital ‘View requests’ page - Hospitals should be able to see the list of all the receivers who have requested
for particular blood group from its blood bank.<br>
<b>Technologies:</b><br>
a) Please write the front-end in HTML/CSS/JS. You may use Bootstrap if you wish.<br>
b) Please write the backend in either core PHP or PHP Codeigniter framework. Use MySQL as the database.<br>
Mainly, we want you to concentrate on the following things:<br>
a) Neat & simple design<br>
b) Good database architecture<br>
c) Good coding practices (write readable, modular code)<br>
Deadline: Please submit the assignment latest by 1st May, 2:00 PM.<br>
How to send: please zip the assignment, upload on google drive and share the google drive link only. Please make sure you include the SQL file so that we can replicate your database.</p>